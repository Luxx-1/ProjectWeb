            Didorong oleh kesuksesan turnamen sepak bola Olimpiade, FIFA, yang dipimpin oleh Presiden Jules Rimet, mulai mencari kesempatan untuk menyelenggarakan turnamen sepak bola internasional yang terpisah dari Olimpiade. Pada 28 Mei 1928, Kongres FIFA di Amsterdam memutuskan akan menggelar kejuaraan dunia sendiri.[16] Karena Uruguay adalah pemegang dua kali gelar kejuaraan sepak bola dunia pada saat itu, dan juga dalam rangka memperingati seratus tahun kemerdekaan mereka pada tahun 1930, FIFA menetapkan Uruguay sebagai negara tuan rumah turnamen Piala Dunia pertama.
            </p>
            <p>
            Dua pertandingan Piala Dunia pertama berlangsung dalam waktu yang bersamaan pada tanggal 13 Juli 1930, pertandingan ini dimenangkan oleh Prancis dan Amerika Serikat, yang masing-masingnya mengalahkan Meksiko 4–1 dan Belgia 3–0. Gol pertama dalam sejarah Piala Dunia dicetak oleh Lucien Laurent dari Prancis.[17] Pada babak final, Uruguay mengalahkan Argentina dengan skor 4–2 di depan kerumunan 93.000 penonton di Montevideo, dan dengan demikian menjadi negara pertama yang menjuarai Piala Dunia.
            </p>
            <p>Piala Dunia 1950, yang diadakan di Brasil, adalah Piala Dunia pertama yang diikuti oleh negara-negara Britania (Skotlandia dan Inggris). Britania Raya keluar dari FIFA pada tahun 1920, sebagian disebabkan oleh keengganan untuk bertanding dengan negara-negara yang pernah berperang dengan mereka, dan sebagian lagi sebagai bentuk protes atas pengaruh asing dalam cabang sepak bola yang mereka ciptakan.[21] Britania akhirnya kembali bergabung pada tahun 1946 setelah diundang secara khusus oleh FIFA.[22] Turnamen 1950 juga kembali diikuti oleh Uruguay yang memboikot dua Piala Dunia sebelumnya. Uruguay sekali lagi menjuarai turnamen setelah mengalahkan tuan rumah Brasil dalam pertandingan yang dijuluki "Maracanazo"
            </p>
            <p>
            Peserta turnamen ditambah menjadi 24 tim pada tahun 1982,[24] dan kemudian ditambah lagi menjadi 32 tim pada 1998,[25] yang sekaligus memungkinkan lebih banyak tim dari Afrika, Asia, dan Amerika Utara untuk ikut berpartisipasi. Sejak saat itu, tim dari kawasan-kawasan ini telah menjadi lebih sukses, beberapa di antaranya telah mencapai babak perempat final, yakni Meksiko pada 1986; dan Kamerun pada 1990. Korea Selatan bahkan menempati posisi keempat pada tahun 2002, sedangkan Senegal menembus perempat final pada tahun yang sama. Tim non-Eropa dan Amerika Selatan terakhir yang sukses melaju ke babak perempat final adalah Ghana pada tahun 2010. Meskipun demikian, tim-tim Eropa dan Amerika Selatan masih saja mendominasi, misalnya dalam perempat final Piala Dunia 1994, 1998, dan 2006, yang kesemua timnya berasal dari Eropa atau Amerika Selatan.
